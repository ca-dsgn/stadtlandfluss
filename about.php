		<h2>Die Entstehungsgeschichte</h2>
<p>Das Projekt "StadtLandFluss-Geschichten" beruht auf einer Kooperation des Studiengangs Onlinemedien an der DHBW Mosbach und den Dokumentarfilmern der DOK-WERK Filmkooperative aus Köln. Diese Akteure stehen für die beiden wesentlichen Bestandteile des Projekts: Web-Technologie und Web-Design einerseits, und Film bzw. Dokumentarfilm andererseits. Das Projekt bildet einen gemeinsamen konzeptionellen Bezugsrahmen für die in der Lehre an der Hochschule im Rahmen des Curriculums "Audiovisuelle Gestaltung" immer wieder neu entstehenden Dokumentarfilme. Diese sollen nicht einfach gesammelt und archivert, sondern innerhalb eines wachsenden "Online-Filmwerks" zusammengesetzt und veröffentlicht werden.</p>

<h2>Zielsetzung des Projekts innerhalb der Lehre</h2>
<p>Die StadtLandFluss-Geschichten sollen es ermöglichen, in Zukunft auch mit den sich aktuell rasch entwickelnden technischen Möglichkeiten zu experimentieren, die das Web für die Gestaltung filmischer Erzählungen zur Verfügung stellt. Derzeit schon realisiert ist die eine interaktive Landkarte, auf der Episoden nach ihrer geografischen Ordnung angeordnet, gefunden und ausgewählt werden können und die Entwicklung einer grafischen Benutzeroberfläche für das Zusammenstellen von "Playlists". Komplexere Beispiele, die in Zukunft realisert werden sollen, könnten z.B. das Einblenden von Information außerhalb des primären Video-Streams zu bestimmten Zeitpunkten oder auch das interaktive Navigieren im Filmmaterial selbst betreffen. Obwohl derzeit noch niemand genau weiß, was die Zukunft hier bringen wird, möchten wir bei dieser Entwicklung dabei sein.</p>

<h2>Politische Implikationen</h2>
<p>Aufgrund ihrer inhaltlichen Ausrichtung haben die StadtLandFluss-Geschichten auch eine zusätzliche kultur- und gesellschaftspolitische Dimension: Die Filme zeichnen ein authentisches Portrait der regionalen Lebenswelt im Neckar-Odenwald-Kreis und beziehen damit Position gegen eine sich überall ausbreitende "Entlokalisierung", Verflachung und Standardisierung von Kultur, die wir allenthalben - letztlich als Folge der Globalisierung - beobachten können. So betrachtet hat das Potential auch das Potential, eine Community-Bildung in der Region zu bewirken und zu fördern. Die Bewohner der Region sollen daher über ein begleitendes Social Media Projekt (auf Facebook) angesprochen werden. Interessant und wünschenswert wäre auch, relevante regionale Institutionen - von kulturellen Landesinstitutionen über die Stadt Mosbach bis hin zu lokalen Institutionen, Vereinen und Gewerbetreibenden - für eine Zusammenarbeit zu gewinnen.</p>

